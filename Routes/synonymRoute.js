// 📋 LIST SYNONYMS
// =========================
router.get("/list", async (req, res) => {

  try {

    const { store } = req.query;

    const normalizedStore =
      store?.trim()?.toLowerCase();

    if (!store) {

      return res.status(400).json({
        error: "Store is required"
      });

    }

    const synonyms =
      await Synonym
        .find({
          store:
            normalizedStore
        })
        .sort({
          createdAt: -1
        })
        .lean();

    res.json({
      synonyms
    });

  } catch (err) {

    console.error(err);

    res.status(500).json({
      error: err.message
    });

  }

});

// =========================
// 🗑 DELETE SYNONYM
// =========================
router.delete("/delete/:id", async (req, res) => {

  try {

    const { store } = req.body;

    const normalizedStore =
      store?.trim()?.toLowerCase();

    if (!store) {

      return res.status(400).json({
        error: "Store is required"
      });

    }

    await Synonym.findOneAndDelete({

      _id: req.params.id,

      store:
        normalizedStore

    });

    res.json({
      success: true
    });

  } catch (err) {

    console.error(err);

    res.status(500).json({
      error: err.message
    });

  }

});

module.exports = router;