router.get("/analytics/search-trends", async (req, res) => {
  try {
    const parsedDays =
      Number(req.query.days);

    const days =

      Number.isNaN(parsedDays) ||

        parsedDays <= 0 ||

        parsedDays > 365

        ? 7

        : parsedDays;

    const startDate = new Date();
    startDate.setDate(startDate.getDate() - days);
    const { store } = req.query;
    if (!store) {
      return res.status(400).json({
        error: "Store is required"
      });
    }
    const normalizedStore =
      store?.trim()?.toLowerCase();
    const data = await Analytics.aggregate([
      {
        $match: {

          type: "search",

          normalizedQuery: {
            $ne: ""
          },

          createdAt: {
            $gte: startDate
          },

          store:
            normalizedStore

        },
      },
      {
        $group: {
          _id: {
            date: {
              $dateToString: {
                format: "%Y-%m-%d",
                date: "$createdAt",
              },
            },
          },
          count: { $sum: 1 },
        },
      },
      {
        $project: {

          _id: 0,

          date:
            "$_id.date",

          count: 1

        }
      },
      {
        $sort: { date: 1 },
      },
    ]);

    if (
      process.env.NODE_ENV !==
      "production"
    ) {

      console.log(
        "SEARCH TRENDS:",
        data
      );

    }

    res.json(data);

  } catch (err) {
    console.error(err);
    res.status(500).json({ error: err.message });
  }
});

router.get("/analytics/stats/all", async (req, res) => {
  try {
    const totalSearches = await Analytics.countDocuments({
      type: "search",
      store: {
        $exists: true,
        $ne: ""
      }
    });

    const totalClicks = await Analytics.countDocuments({
      type: "click",
      store: {
        $exists: true,
        $ne: ""
      }
    });

    const stores = await Analytics.aggregate([
      {
        $match: {
          store: {
            $exists: true,
            $ne: ""
          }
        }
      },
      {
        $group: {
          _id: "$store",
          totalSearches: {
            $sum: { $cond: [{ $eq: ["$type", "search"] }, 1, 0] }
          },
          totalClicks: {
            $sum: { $cond: [{ $eq: ["$type", "click"] }, 1, 0] }
          }
        }
      }
    ]);

    const storesWithConversion = stores.map(s => ({
      ...s,
      conversionRate:
        s.totalSearches === 0
          ? 0
          : ((s.totalClicks / s.totalSearches) * 100).toFixed(1)
    }));

    res.json({
      totals: {
        totalSearches,
        totalClicks,
        conversionRate:
          totalSearches === 0
            ? 0
            : ((totalClicks / totalSearches) * 100).toFixed(1)
      },
      stores: storesWithConversion
    });

  } catch (err) {
    console.error(err);
    res.status(500).json({
      error: err.message
    });

  }
});

