const express = require("express");
const router = express.Router();
const Analytics = require("../Models/analyticsModel");
const stringSimilarity = require("string-similarity");
const Synonym = require("../Models/synonymModel");

router.post("/analytics", async (req, res) => {

  try {

    const {
      vendor,
      type,
      query,
      productId,
      resultsCount: incomingResultsCount,
      productHandle,
      store,
      productTitle,
      productImage
    } = req.body;

    const allowedTypes = [
      "search",
      "click",
      "no_result"
    ];

    if (
      !allowedTypes.includes(type)
    ) {

      return res.status(400).json({
        error:
          "Invalid analytics type"
      });

    }

    if (
      process.env.NODE_ENV !==
      "production"
    ) {

      console.log(
        "BODY RECEIVED:",
        req.body
      );

    }

    if (!store) {

      return res.status(400).json({
        error:
          "Store is required"
      });

    }

    if (
      type === "search" &&
      !query?.trim()
    ) {

      return res.json({
        success: false
      });

    }

    const normalizedStore =
      store
        ?.trim()
        ?.toLowerCase();

    const normalizedQuery =
      query
        ?.trim()
        ?.toLowerCase();

    const userAgent =
      req.headers[
      "user-agent"
      ] || "";

    const resultsCount =
      type === "no_result"
        ? 0
        : Number(
          incomingResultsCount
        ) || 0;

    // =====================================
    // 🔥 SAVE ANALYTICS
    // =====================================

    await Analytics.create({

      type,

      query:
        query?.trim() || "",

      normalizedQuery,

      productId,

      productTitle:
        productTitle || null,

      productHandle:
        productHandle || null,

      productImage:
        productImage || null,

      vendor:
        vendor
          ?.trim()
          ?.toLowerCase() || null,

      store:
        normalizedStore,

      device:
        /ipad|tablet/i.test(
          userAgent
        )
          ? "tablet"
          : /mobile|android|iphone/i.test(
            userAgent
          )
            ? "mobile"
            : "desktop",
      resultsCount

    });

    // =====================================
    // 🔥 AUTO LEARN SYNONYMS
    // =====================================

    if (
      type === "search" &&
      normalizedQuery
    ) {

      const synonymGroups =
        await Synonym.find({

          store:
            normalizedStore,

          query: {
            $ne:
              normalizedQuery
          }

        })
          .select("query synonyms")
          .limit(50);

      for (const item of synonymGroups) {

        if (
          item.query ===
          normalizedQuery
        ) {
          continue;
        }

        const score =
          stringSimilarity
            .compareTwoStrings(

              normalizedQuery,

              item.query

            );

        // TYPO / CLOSE MATCH
        if (
          score >= 0.86 &&
          Math.abs(
            normalizedQuery.length -
            item.query.length
          ) <= 2
        ) {

          const existingSynonym =
            item.synonyms.find(

              s =>
                s.word ===
                normalizedQuery

            );

          // UPDATE EXISTING
          if (existingSynonym) {

            existingSynonym.usageCount += 1;

            existingSynonym.confidence =
              Math.max(

                existingSynonym.confidence || 0,

                score

              );

          }

          // SAVE ONLY AFTER
          // 10 SEARCHES

          if (!existingSynonym) {

            const alreadyTooMany =
              item.synonyms.length >= 20;
            if (alreadyTooMany) {
              continue;
            }
            item.synonyms.push({
              word:
                normalizedQuery,
              usageCount: 1,
              autoGenerated: true,
              confidence: score
            });

          }

          if (
            !existingSynonym ||
            existingSynonym.usageCount % 5 === 0
          ) {
            await item.save();

          }
        }
      }

    }

    res.json({
      success: true
    });

  } catch (err) {

    console.error(err);

    res.status(500).json({
      error: err.message
    });

  }

});

router.get("/analytics/stats", async (req, res) => {
  try {
    const now = new Date();

    const last7Days = new Date();
    last7Days.setDate(now.getDate() - 7);

    const prev7Days = new Date();
    prev7Days.setDate(now.getDate() - 14);

    const { store } = req.query;
    if (!store) {
      return res.status(400).json({
        error: "Store is required"
      });
    }
    const normalizedStore =
      store?.trim()?.toLowerCase();

    const [

      currentSearches,
      prevSearches,
      currentClicks,
      prevClicks
    ] = await Promise.all([
      // CURRENT SEARCHES
      Analytics.countDocuments({
        type: "search",
        store:
          normalizedStore,
        createdAt: {
          $gte:
            last7Days
        }
      }),

      // PREVIOUS SEARCHES
      Analytics.countDocuments({
        type: "search",
        store:
          normalizedStore,
        createdAt: {
          $gte:
            prev7Days,
          $lt:
            last7Days
        }
      }),

      // CURRENT CLICKS
      Analytics.countDocuments({
        type: "click",
        store:
          normalizedStore,
        createdAt: {
          $gte:
            last7Days
        }
      }),

      // PREVIOUS CLICKS
      Analytics.countDocuments({
        type: "click",
        store:
          normalizedStore,
        createdAt: {
          $gte:
            prev7Days,
          $lt:
            last7Days
        }
      })
    ]);

    // 🔥 FUNCTION
    const calcGrowth = (current, prev) => {
      if (prev === 0) {
        return current > 0
          ? "+100%"
          : "0%";
      }
      const growth =
        (
          (
            (current - prev) /
            prev
          ) * 100
        ).toFixed(1);

      return `${growth > 0 ? "+" : ""
        }${growth}%`;
    };

    res.json({
      totalSearches:
        currentSearches,
      searchesGrowth:
        calcGrowth(
          currentSearches,
          prevSearches
        ),
      totalClicks:
        currentClicks,
      clicksGrowth:
        calcGrowth(
          currentClicks,
          prevClicks
        )
    });

  } catch (err) {
    console.error(err);
    res.status(500).json({
      error: err.message
    });
  }
});

