router.get("/analytics/overview", async (req, res) => {

  try {

    const { store } = req.query;

    if (!store) {

      return res.status(400).json({
        error: "Store is required"
      });

    }

    const parsedDays =
      Number(req.query.days);

    const days =

      Number.isNaN(parsedDays) ||

        parsedDays <= 0 ||

        parsedDays > 365

        ? 7

        : parsedDays;

    const normalizedStore =
      store
        ?.trim()
        ?.toLowerCase();

    // =========================
    // 🔥 DATE RANGE
    // =========================

    const startDate =
      new Date();

    startDate.setDate(
      startDate.getDate() - days
    );

    // =========================
    // 🔥 PREVIOUS RANGE
    // =========================

    const prevStartDate =
      new Date();

    prevStartDate.setDate(
      prevStartDate.getDate() -
      days * 2
    );

    // =========================
    // 🔥 PARALLEL QUERIES
    // =========================

    const [

      totalSearches,

      prevSearches,

      totalClicks,

      zeroResults,

      avgResultsData

    ] = await Promise.all([

      // CURRENT SEARCHES
      Analytics.countDocuments({

        type: "search",

        store:
          normalizedStore,

        createdAt: {
          $gte: startDate
        }

      }),

      // PREVIOUS SEARCHES
      Analytics.countDocuments({

        type: "search",

        store:
          normalizedStore,

        createdAt: {

          $gte:
            prevStartDate,

          $lt:
            startDate

        }

      }),

      // TOTAL CLICKS
      Analytics.countDocuments({

        type: "click",

        store:
          normalizedStore,

        createdAt: {
          $gte: startDate
        }

      }),

      // ZERO RESULTS
      Analytics.countDocuments({

        type: "search",

        store:
          normalizedStore,

        resultsCount: 0,

        createdAt: {
          $gte: startDate
        }

      }),

      // AVG RESULTS
      Analytics.aggregate([

        {

          $match: {

            type: "search",

            store:
              normalizedStore,

            createdAt: {
              $gte: startDate
            }

          }

        },

        {

          $group: {

            _id: null,

            avgResults: {
              $avg:
                "$resultsCount"
            }

          }

        }

      ])

    ]);

    // =========================
    // 🔥 GROWTH FUNCTION
    // =========================

    const calcGrowth = (
      current,
      previous
    ) => {

      if (previous === 0) {

        return current > 0
          ? "+100%"
          : "0%";

      }

      const growth = (

        (
          (
            current - previous
          ) / previous
        ) * 100

      ).toFixed(1);

      return `${growth > 0 ? "+" : ""
        }${growth}%`;

    };

    // =========================
    // 🔥 CTR
    // =========================

    const ctr =
      totalSearches === 0
        ? 0
        : (

          (
            totalClicks /
            totalSearches
          ) * 100

        ).toFixed(1);

    // =========================
    // 🔥 ZERO RESULT RATE
    // =========================

    const zeroResultRate =
      totalSearches === 0
        ? 0
        : (

          (
            zeroResults /
            totalSearches
          ) * 100

        ).toFixed(1);

    // =========================
    // 🔥 RESPONSE
    // =========================

    res.json({

      days,

      totalSearches,

      totalClicks,

      ctr,

      zeroResultRate,

      avgResults:

        avgResultsData?.length

          ? avgResultsData[0]
            .avgResults
            .toFixed(1)

          : 0,

      searchGrowth:
        calcGrowth(

          totalSearches,

          prevSearches

        ),

      ctrGrowth: "0%",

      zeroResultGrowth:
        "0%"

    });

  } catch (err) {

    console.error(
      "OVERVIEW ERROR:",
      err
    );

    res.status(500).json({
      error: err.message
    });

  }

});

